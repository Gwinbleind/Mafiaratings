function drawPlayerRatingChart() {
  const canvas = document.getElementById("playerRatingChart");
  if (!canvas || window.innerWidth <= 980) {
    return;
  }
  const ctx = canvas.getContext("2d");
  if (!ctx) {
    return;
  }

  const labels = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"];
  const values = [2420, 2475, 2510, 2550, 2580, 2625, 2610, 2640, 2665, 2650, 2670, 2685];
  const max = Math.max(...values) + 40;
  const min = Math.min(...values) - 40;

  const w = canvas.width;
  const h = canvas.height;
  const padL = 56;
  const padR = 24;
  const padT = 20;
  const padB = 42;
  const chartW = w - padL - padR;
  const chartH = h - padT - padB;

  ctx.clearRect(0, 0, w, h);
  ctx.fillStyle = "#ffffff";
  ctx.fillRect(0, 0, w, h);

  ctx.strokeStyle = "#e5e7eb";
  ctx.lineWidth = 1;
  for (let i = 0; i <= 4; i++) {
    const y = padT + (chartH / 4) * i;
    ctx.beginPath();
    ctx.moveTo(padL, y);
    ctx.lineTo(w - padR, y);
    ctx.stroke();
  }

  ctx.fillStyle = "#6b7280";
  ctx.font = "12px Inter, Segoe UI, Arial, sans-serif";
  for (let i = 0; i < labels.length; i++) {
    const x = padL + (chartW / (labels.length - 1)) * i;
    ctx.fillText(labels[i], x - 10, h - 18);
  }

  ctx.strokeStyle = "#5b6cff";
  ctx.lineWidth = 3;
  ctx.beginPath();
  values.forEach((v, i) => {
    const x = padL + (chartW / (values.length - 1)) * i;
    const y = padT + ((max - v) / (max - min)) * chartH;
    if (i === 0) ctx.moveTo(x, y);
    else ctx.lineTo(x, y);
  });
  ctx.stroke();

  values.forEach((v, i) => {
    const x = padL + (chartW / (values.length - 1)) * i;
    const y = padT + ((max - v) / (max - min)) * chartH;
    ctx.beginPath();
    ctx.fillStyle = "#ffffff";
    ctx.arc(x, y, 4, 0, Math.PI * 2);
    ctx.fill();
    ctx.lineWidth = 2;
    ctx.strokeStyle = "#5b6cff";
    ctx.stroke();
  });

  ctx.fillStyle = "#111827";
  ctx.font = "600 12px Inter, Segoe UI, Arial, sans-serif";
  ctx.fillText(String(values[values.length - 1]), w - padR - 36, padT + 12);
}

drawPlayerRatingChart();
window.addEventListener("resize", drawPlayerRatingChart);
